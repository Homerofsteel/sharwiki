  async function getPlayerNameById(playerId) {
    const url = `https://www.speedrun.com/api/v1/users/${playerId}`;
    try {
        const response = await fetch(url);
        if (!response.ok) {
            throw new Error(`Erreur HTTP: ${response.status}`);
        }
        const data = await response.json();
        
        // Vérifier si le nom international existe et retourner un tableau avec l'ID et le nom
        if (data.data.names && data.data.names.international) {
            return [playerId, data.data.names.international];
        } else {
            return [playerId, "Nom inconnu"];
        }
    } catch (error) {
        console.error(`Erreur lors de la récupération du joueur ${playerId}: ${error}`);
        return [playerId, "Erreur lors de la récupération"];
    }
}

// Fonction pour ajouter un délai entre les requêtes
function delay(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

// Fonction principale pour parcourir la liste des IDs et récupérer les noms sous le format désiré
async function getPlayerNames() {
    const playersData = [];
    
    // Parcours des playerIds un par un, avec délai entre chaque requête
    for (const playerId of playerIds) {
        const playerName = await getPlayerNameById(playerId);
        playersData.push(playerName);
        console.log(`Nom récupéré pour ${playerId}: ${playerName[1]}`);
        
        // Attente de 1 seconde (1000 ms) avant la prochaine requête
        await delay(1000);
    }
    return playersData;
}

